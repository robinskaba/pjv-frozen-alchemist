package cz.cvut.fel.pjv.skabaro2.frozen_alchemist.view;

public class PixelPosition {
    private double x;
    private double y;

    public PixelPosition(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
