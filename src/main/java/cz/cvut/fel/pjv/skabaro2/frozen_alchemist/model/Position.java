package cz.cvut.fel.pjv.skabaro2.frozen_alchemist.model;

public class Position {
    private int x = 0;
    private int y = 0;

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public Position() {}
}
